package loops;

public class ForEach {

	public static void main(String[] args) {
int a[]={1,2,3,4,5};
		
		for(int i:a){
			System.out.println(i);
			
		}
		
		String st[]={"anjali","raj","mahesh","qaplanet","hyderabad"};
		
		for(String i:st){
			System.out.println(i);
		}


	}

}
